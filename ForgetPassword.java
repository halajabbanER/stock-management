    String enteredEmail = jTextField1.getText();
    String newEnteredPassword = new String(jPasswordField1.getPassword());

    // Connect to your database
    try {
        Connection connection = DriverManager.getConnection(url, "root", "hala2211");
        String sql = "UPDATE users SET password = ? WHERE email = ?";
        try (PreparedStatement statement = connection.prepareStatement(sql)) {
            statement.setString(1, newEnteredPassword);
            statement.setString(2, enteredEmail);

            // Execute the update
            int rowsAffected = statement.executeUpdate();
            if (rowsAffected > 0) {
                // Password reset successful
                JOptionPane.showMessageDialog(null, "Password reset successful.", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                // No rows affected - email not found
                JOptionPane.showMessageDialog(null, "Email not found. Please try again.", "Password Reset Failed", JOptionPane.ERROR_MESSAGE);
            }
        }
    } catch (SQLException ex) {
        // Handle any SQL exceptions
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "An error occurred. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
    }
